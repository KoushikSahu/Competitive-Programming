int n;

void solve(){
  cin>>n;
  vint ans, fin_ans;
  for(int i=1; i<n; i++){
    if(__gcd(i, n)==1){
      ans.push_back(i);
    }
  }
  long long rem = 1;
  for(int i: ans){
    rem *= i;
    rem %= n;
  }
  for(int i: ans){
    if(rem==1 || i!=rem){
      fin_ans.push_back(i);
    }
  }
  cout<<sz(fin_ans)<<'\n';
  for(int i: fin_ans) cout<<i<<' ';
  cout<<'\n';
}

int main(){
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  int T = 1;
  // cin>>T; 
  while(T--){
    solve();
  }
  return 0;
}
