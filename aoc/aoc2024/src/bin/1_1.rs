fn solve<R: BufRead, W: Write>(sc: &mut Scanner<R>, wr: &mut W) {
    let mut a: Vec<i32> = Vec::new();
    let mut b: Vec<i32> = Vec::new();
    while let (Some(aa), Some(bb)) = (sc.try_next::<i32>(), sc.try_next::<i32>()) {
        a.push(aa);
        b.push(bb);
    }
    let mut b_cntr: BTreeMap<i32, i32> = BTreeMap::new();
    for i in 0..b.len() {
        b_cntr.entry(b[i]).and_modify(|val| *val += 1).or_insert(1);
    }
    let mut ans = 0;
    for i in 0..a.len() {
        ans += a[i] * b_cntr.get(&a[i]).unwrap_or(&0);
    }
    writeln!(wr, "{}", ans).ok();
}

